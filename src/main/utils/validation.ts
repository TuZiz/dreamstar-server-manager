import type {
  AppConfig,
  DatabaseConnectionConfig,
  ServerInstanceConfig,
  ServerPropertiesConfig
} from '../../shared/types';

export const SERVER_ID_PATTERN = /^[a-zA-Z0-9_-]+$/;

export function assertValidId(id: string, label = 'ID'): void {
  if (!id.trim()) {
    throw new Error(`${label} 不能为空`);
  }
  if (!SERVER_ID_PATTERN.test(id)) {
    throw new Error(`${label} 只允许字母、数字、短横线和下划线`);
  }
}

export function assertUniqueServer(config: AppConfig, server: Pick<ServerInstanceConfig, 'id' | 'name' | 'workdir'>): void {
  assertValidId(server.id, '实例 ID');
  if (!server.name.trim()) {
    throw new Error('实例名称不能为空');
  }
  if (!server.workdir.trim()) {
    throw new Error('工作目录不能为空');
  }
  if (config.servers.some((item) => item.id === server.id)) {
    throw new Error('实例 ID 已存在');
  }
  if (config.servers.some((item) => item.name.trim() === server.name.trim())) {
    throw new Error('实例名称已存在');
  }
  const normalizedWorkdir = server.workdir.trim().toLowerCase();
  if (config.servers.some((item) => item.workdir.trim().toLowerCase() === normalizedWorkdir)) {
    throw new Error('该工作目录已经绑定到其他实例');
  }
}

export function assertUniqueDatabase(config: AppConfig, database: Pick<DatabaseConnectionConfig, 'id' | 'name'>): void {
  assertValidId(database.id, '数据库连接 ID');
  if (!database.name.trim()) {
    throw new Error('数据库连接名称不能为空');
  }
  if (config.databases.some((item) => item.id === database.id)) {
    throw new Error('数据库连接 ID 已存在');
  }
  if (config.databases.some((item) => item.name.trim() === database.name.trim())) {
    throw new Error('数据库连接名称已存在');
  }
}

export function splitCommandLine(value = ''): string[] {
  const result: string[] = [];
  const pattern = /"([^"\\]*(?:\\.[^"\\]*)*)"|'([^'\\]*(?:\\.[^'\\]*)*)'|[^\s]+/g;
  for (const match of value.matchAll(pattern)) {
    result.push((match[1] ?? match[2] ?? match[0]).replace(/\\"/g, '"').replace(/\\'/g, "'"));
  }
  return result;
}

