export function serializeServerProperties(properties: ServerPropertiesConfig): string {
  const lines = [
    '# Generated by DreamStar Server Manager',
    `server-port=${properties.serverPort}`,
    `online-mode=${properties.onlineMode}`,
    `motd=${properties.motd}`,
    `max-players=${properties.maxPlayers}`,
    `view-distance=${properties.viewDistance}`,
    `simulation-distance=${properties.simulationDistance}`,
    `enable-command-block=${properties.enableCommandBlock}`,
    `allow-flight=${properties.allowFlight}`,
    `difficulty=${properties.difficulty}`,
    `gamemode=${properties.gamemode}`,
    `pvp=${properties.pvp}`,
    `spawn-protection=${properties.spawnProtection}`
  ];
  return `${lines.join('\n')}\n`;
}

export function mergeServerProperties(existing: string, generated: string): string {
  const existingLines = existing.split(/\r?\n/);
  const existingKeys = new Set(
    existingLines
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith('#') && line.includes('='))
      .map((line) => line.split('=')[0])
  );

  const missingLines = generated
    .split(/\r?\n/)
    .filter((line) => {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#') || !trimmed.includes('=')) {
        return false;
      }
      return !existingKeys.has(trimmed.split('=')[0]);
    });

  if (missingLines.length === 0) {
    return existing.endsWith('\n') ? existing : `${existing}\n`;
  }

  const suffix = ['# Added by DreamStar Server Manager', ...missingLines].join('\n');
  return `${existing.trimEnd()}\n${suffix}\n`;
}

const DANGEROUS_SQL = /^(insert|update|delete|drop|truncate|alter|create|grant|revoke)\b/i;

export function isDangerousSql(sql: string): boolean {
  return DANGEROUS_SQL.test(sql.trim());
}

export function getSqlWarning(sql: string): string | undefined {
  const normalized = sql.trim();
  if (/^select\s+\*\s+from\s+/i.test(normalized) && !/\blimit\s+\d+/i.test(normalized)) {
    return 'SELECT * 未包含 LIMIT，建议先限制返回行数。';
  }
  return undefined;
}

const DANGEROUS_REDIS = new Set(['DEL', 'FLUSHDB', 'FLUSHALL', 'CONFIG', 'SHUTDOWN', 'EVAL']);

export function isDangerousRedisCommand(command: string): boolean {
  const name = command.trim().split(/\s+/)[0]?.toUpperCase();
  return DANGEROUS_REDIS.has(name);
}
